#!/usr/bin/env node
// Sprint 5: emit a default MSW handler per (method, path) from the canonical
// OpenAPI doc. Run via `make frontend-mocks-regen` whenever the contract
// changes (i.e. after `make openapi-emit`).
//
// Each handler returns `HttpResponse.json({}, { status: 200 })`. Tests that
// need realistic payloads override `parameters.msw.handlers` (see
// src/shared/api/mocks/README.md). The point is to give every endpoint a default so
// MSW never treats a request as unhandled — the unhandled-request policy is
// `bypass`, but having seed handlers makes overrides cheaper to write.

import { readFileSync, writeFileSync } from 'node:fs'
import path from 'node:path'
import url from 'node:url'
import { load } from 'js-yaml'

const __filename = url.fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..', '..', '..')
const yamlPath = path.join(repoRoot, 'apps/api/contracts/openapi.gen.yaml')
const outPath = path.resolve(__dirname, '..', 'src/shared/api/mocks/handlers.ts')

const METHODS = ['get', 'post', 'put', 'patch', 'delete']

const doc = load(readFileSync(yamlPath, 'utf8'))
if (!doc || typeof doc !== 'object' || !doc.paths) {
  throw new Error(`No paths found in ${yamlPath}`)
}

const seen = new Set()
const lines = []
for (const [openapiPath, item] of Object.entries(doc.paths)) {
  // Convert `/v1/foo/{bar}` -> `/v1/foo/:bar` for MSW path syntax.
  const mswPath = openapiPath.replaceAll('{', ':').replaceAll('}', '')
  for (const method of METHODS) {
    if (!item[method]) continue
    const operationId = item[method].operationId ?? `${method}_${openapiPath}`
    const key = `${method.toUpperCase()} ${mswPath}`
    if (seen.has(key)) continue
    seen.add(key)
    lines.push(
      `  http.${method}(${JSON.stringify(mswPath)}, () => HttpResponse.json({}, { status: 200 })), // ${operationId}`,
    )
  }
}

const banner = `// AUTO-GENERATED by scripts/gen-msw-handlers.mjs from
// apps/api/contracts/openapi.gen.yaml. Do not edit by hand — regenerate via
// \`make frontend-mocks-regen\`. Override per-test with parameters.msw.handlers
// or server.use(http.<method>(...)) inside an arrange block.

import { http, HttpResponse } from "msw";

export const handlers = [
${lines.join('\n')}
];
`

writeFileSync(outPath, banner)
console.log(`gen-msw-handlers: wrote ${lines.length} handlers to ${outPath}`)
